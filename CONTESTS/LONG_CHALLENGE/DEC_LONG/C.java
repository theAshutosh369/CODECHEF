import java.util.*;

public class C {

    public static void main ( String[] args ) {

        Scanner scann = new Scanner(System.in);
        int random = scann.nextInt();
        while (random-- >0) {
            String str1 = scann.next();
            String str2 = scann.next();
            int length = str1.length();

            int s1_zero=0 , s1_one=0 , s2_zero=0 , s2_one=0  ;
            for (int i = 0; i < length; i++)
            {
                if( str1.charAt(i)=='1'){
                    s1_one++;
                }
            }
            for (int i = 0; i < length; i++)
            {
                if( str2.charAt(i)=='1'){
                    s2_one++;
                }
            }

            s1_zero= length-s1_one;
            s2_zero= length-s2_one;

            int ones = Math.min( s1_zero , s2_one) + Math.min( s2_zero, s1_one );

            for (int index = 0; index < length; index++) {
                if( index<ones){
                    System.out.print("1");
                }
                else {
                    System.out.print("0");
                }
            }
            System.out.println();

        }
    }
}

